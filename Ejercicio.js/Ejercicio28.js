// Calcular el sueldo de un empleado dados como datos de entrada:
// a. Nombre.
// b. horas de trabajo.
// c. pago en hora
let nombre=prompt("Ingrese su nombre");
let horas_t=prompt("Ingrese las horas que labora");
let pagoXhora=5.652;
let procedimiento=horas_t*pagoXhora;
alert(`Su nombre es ${nombre} Usted trabajo ${horas_t} y su sueldo es de ${procedimiento}`)